def загрузить_статы():
    try:
        if os.path.exists(ФАЙЛ_СТАТОВ):
            with open(ФАЙЛ_СТАТОВ, "r", encoding="utf-8") as f:
                return json.load(f)
    except (json.JSONDecodeError, IOError) as e:
        log.error(f"Ошибка загрузки статов: {e}")
    return {}

def сохранить_статы(статы):
    try:
        with open(ФАЙЛ_СТАТОВ, "w", encoding="utf-8") as f:
            json.dump(статы, f, ensure_ascii=False, indent=2)
    except IOError as e:
        log.error(f"Ошибка сохранения статов: {e}")

def получить_игрока(статы, user_id, имя=None):
    ключ = str(user_id)
    if ключ not in статы:
        статы[ключ] = {"имя": имя or "незнакомец", "игр": 0, "выжил": 0, "рекорд": 0, "ачивки": [], "пасхалки": []}
    игрок = статы[ключ]
    if "ачивки" not in игрок:
        игрок["ачивки"] = []
    if "пасхалки" not in игрок:
        игрок["пасхалки"] = []
    return игрок

def получить_имя(user_id):
    статы = загрузить_статы()
    игрок = статы.get(str(user_id))
    if игрок and игрок.get("имя"):
        return игрок["имя"]
    return None

def сохранить_имя(user_id, имя):
    статы = загрузить_статы()
    игрок = получить_игрока(статы, user_id, имя)
    игрок["имя"] = имя
    сохранить_статы(статы)

