def старт_казика(chat_id, user_id, имя):
    колода = новая_колода()
    рука = [колода.pop(), колода.pop()]
    дилер = [колода.pop(), колода.pop()]
    казик[chat_id] = {"user_id": user_id, "рука": рука, "дилер": дилер, "колода": колода}
    сумма = сумма_руки(рука)
    текст = (
        f"🎰 *Блэкджек — /казик*\n\n"
        f"Твои карты: {карты_текст(рука)} — *{сумма}*\n"
        f"Карта дилера: {карты_текст([дилер[0]])} ?\n\n"
        f"_Берёшь ещё карту?_"
    )
    if сумма == 21:
        del казик[chat_id]
        финал_казика(chat_id, user_id, имя, рука, дилер, колода)
        return
    bot.send_message(chat_id, текст, parse_mode="Markdown", reply_markup=кнопки_казика())

def финал_казика(chat_id, user_id, имя, рука, дилер, колода):
    while сумма_руки(дилер) < 17:
        дилер.append(колода.pop())
    сп = сумма_руки(рука)
    сд = сумма_руки(дилер)
    текст = (
        f"🎰 *Результат*\n\n"
        f"Твои карты: {карты_текст(рука)} — *{сп}*\n"
        f"Дилер: {карты_текст(дилер)} — *{сд}*\n\n"
    )
    if сп > 21:
        текст += "💀 *ПЕРЕБОР!* Ты проиграл. Нормалдаки 🔥"
        победа = False
    elif сд > 21 or сп > сд:
        текст += "🏆 *ПОБЕДА!* Изи бриджи 😎"
        победа = True
    elif сп == сд:
        текст += "🤝 *НИЧЬЯ.* Хамамм 🕌"
        победа = False
    else:
        текст += "💀 *ПРОИГРЫШ.* Дилер сильнее. Бем бем 💥"
        победа = False
    статы = загрузить_статы()
    игрок = получить_игрока(статы, user_id, имя)
    игрок.setdefault("казик_игр", 0)
    игрок.setdefault("казик_побед", 0)
    игрок["казик_игр"] += 1
    if победа:
        игрок["казик_побед"] += 1
    сохранить_статы(статы)
    markup = InlineKeyboardMarkup()
    markup.row(
        InlineKeyboardButton("🔄 Ещё раз", callback_data="казик_заново"),
        InlineKeyboardButton("🏆 Топ", callback_data="топ")
    )
    bot.send_message(chat_id, текст, parse_mode="Markdown", reply_markup=markup)

