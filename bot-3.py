def дать_ачивку(chat_id, user_id, имя, ачивка_id):
    if ачивка_id not in ДОСТИЖЕНИЯ:
        return
    статы = загрузить_статы()
    игрок = получить_игрока(статы, user_id, имя)
    if ачивка_id not in игрок["ачивки"]:
        игрок["ачивки"].append(ачивка_id)
        сохранить_статы(статы)
        а = ДОСТИЖЕНИЯ[ачивка_id]
        try:
            bot.send_message(
                chat_id,
                f"🏅 *Новое достижение разблокировано!*\n\n"
                f"{а['emoji']} *{а['название']}*\n"
                f"_{а['описание']}_",
                parse_mode="Markdown"
            )
        except Exception as e:
            log.error(f"Ошибка отправки ачивки: {e}")

def проверить_ачивки_рулетка(chat_id, user_id, имя, выжил, выстрелов_выжито):
    статы = загрузить_статы()
    игрок = получить_игрока(статы, user_id, имя)
    игр = игрок.get("игр", 0)
    побед = игрок.get("выжил", 0)
    ачивки = игрок.get("ачивки", [])

    if игр == 1 and "первая_игра" not in ачивки:
        дать_ачивку(chat_id, user_id, имя, "первая_игра")

    # FIX: выстрелов_выжито == 0 означает смерть на первом выстреле
    if not выжил and выстрелов_выжито == 0 and "смертник" not in ачивки:
        дать_ачивку(chat_id, user_id, имя, "смертник")

    if выжил:
        пороги = [(1, "везунчик"), (3, "на_огне"), (5, "чемпион"),
                  (10, "легенда"), (25, "бессмертный"), (50, "бог_рулетки")]
        for порог, ид in пороги:
            if побед >= порог and ид not in ачивки:
                дать_ачивку(chat_id, user_id, имя, ид)

    if игр >= 50 and "задрот" not in ачивки:
        дать_ачивку(chat_id, user_id, имя, "задрот")

def отметить_пасхалку(chat_id, user_id, имя, ключ_пасхалки):
    статы = загрузить_статы()
    игрок = получить_игрока(статы, user_id, имя)
    if ключ_пасхалки not in игрок["пасхалки"]:
        игрок["пасхалки"].append(ключ_пасхалки)
        сохранить_статы(статы)
    if len(игрок["пасхалки"]) >= len(ALL_СЕКРЕТЫ) and "задрот_полный" not in игрок["ачивки"]:
        дать_ачивку(chat_id, user_id, имя, "задрот_полный")

def обновить_стат(chat_id, user_id, имя, выжил, выстрелов):
    статы = загрузить_статы()
    игрок = получить_игрока(статы, user_id, имя)
    игрок["имя"] = имя
    игрок["игр"] += 1
    if выжил:
        игрок["выжил"] += 1
    if выстрелов > игрок.get("рекорд", 0):
        игрок["рекорд"] = выстрелов
    сохранить_статы(статы)
    проверить_ачивки_рулетка(chat_id, user_id, имя, выжил, выстрелов)

