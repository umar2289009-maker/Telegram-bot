# ─── Обработчик всех сообщений ───────────────────────────────────────────────

@bot.message_handler(func=lambda message: True)
def ответ(message):
    try:
        if message.text is None:
            return

        chat_id = message.chat.id
        user_id = message.from_user.id
        текст = message.text.lower().strip()

        if в_группе(message) and not бот_упомянут(message) and not текст.startswith("/"):
            return

        текст_чистый = текст.replace(f"@{BOT_INFO.username.lower()}", "").strip()

        # Ожидание ника
        if ждёт_имя.get(user_id):
            новый_ник = message.text.strip()
            ждёт_имя[user_id] = False
            сохранить_имя(user_id, новый_ник)
            bot.send_message(chat_id,
                f"Привет лох, {новый_ник} 👋\n\nНажми /start чтобы увидеть всё меню 👇")
            return

        имя = получить_имя(user_id) or message.from_user.first_name or "незнакомец"

        # Пасхалка "дурак"
        if текст_чистый == "дурак":
            bot.send_message(chat_id, f"я тебе глотку вырву, {имя}" if получить_имя(user_id) else "я тебе лопатки вырву")
            отметить_пасхалку(chat_id, user_id, имя, "дурак")
            return

        # Пасхалка "ар з" (даёт +2 победы)
        if "ар з" in текст_чистый:
            статы = загрузить_статы()
            игрок = получить_игрока(статы, user_id, имя)
            игрок["выжил"] += 2
            игрок["игр"] += 2
            сохранить_статы(статы)
            bot.send_message(chat_id,
                f"полная ботиха 🤖\n\n_+2 победы в рулетке добавлено, {имя}_ 🎁",
                parse_mode="Markdown")
            отметить_пасхалку(chat_id, user_id, имя, "ар з")
            return

        # Остальные пасхалки
        for ключ_п, ответы in ПАСХАЛКИ.items():
            if ключ_п in текст_чистый:
                bot.send_message(chat_id, random.choice(ответы))
                отметить_пасхалку(chat_id, user_id, имя, ключ_п)
                return

    except Exception as e:
        log.error(f"ответ handler error: {e}")

