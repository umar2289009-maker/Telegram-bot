    try:
        части = message.text.split(maxsplit=1)
        if len(части) < 2 or not части[1].strip():
            bot.send_message(message.chat.id,
                "😐 А вопрос где?\n\nПример: /спроси почему я лох")
            return

        вопрос = части[1].strip().lower()
        chat_id = message.chat.id

        # Небольшая задержка — бот "думает"
        import threading
        def ответить():
            ответ = None
            for ключ, список in ДОПРОС_ОТВЕТЫ.items():
                if ключ in вопрос:
                    ответ = random.choice(список)
                    break
            if not ответ:
                ответ = random.choice(ДОПРОС_ОБЩИЕ)

            # 10% шанс — бот отвечает максимально коротко
            if random.random() < 0.10:
                ответ = random.choice(["да 😐", "нет 😐", "хамамм 🕌", "67", "нормалдаки 🔥", "изи бриджи 😎"])

            bot.send_message(chat_id, f"🤔 _{вопрос}_\n\n{ответ}", parse_mode="Markdown")

        t = threading.Timer(random.uniform(0.5, 1.5), ответить)
        t.start()
    except Exception as e:
        log.error(f"/спроси error: {e}")

