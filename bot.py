СЛОТ_СИМВОЛЫ = ["🍋", "🍊", "🍇", "🍒", "🔔", "💎", "7️⃣", "🃏"]
СЛОТ_ВЕСА    = [18,   16,   15,   14,   12,    6,    10,    9]

СЛОТ_ВЫПЛАТЫ = {
    ("💎", "💎", "💎"): ("💎 ДЖЕКПОТ! 💎", 25),
    ("7️⃣", "7️⃣", "7️⃣"): ("7️⃣ СЕМЁРКИ! 🔥", 15),
    ("🔔", "🔔", "🔔"): ("🔔 КОЛОКОЛ! 🎉", 8),
}

def прокрутить_слоты():
    return random.choices(СЛОТ_СИМВОЛЫ, weights=СЛОТ_ВЕСА, k=3)

def результат_слотов(барабаны, ставка):
    а, б, в = барабаны
    ключ = tuple(барабаны)
    if ключ in СЛОТ_ВЫПЛАТЫ:
        назв, множ = СЛОТ_ВЫПЛАТЫ[ключ]
        выигрыш = ставка * множ
        return назв, выигрыш, True
    if а == б == в:
        выигрыш = ставка * 5
        return f"{а}{а}{а} — ТРОЙКА!", выигрыш, True
    if а == б or б == в or а == в:
        выигрыш = ставка // 2
        return "Два одинаковых — возврат части 😐", выигрыш, False
    return "Ничего 😶 — мимо", 0, False

