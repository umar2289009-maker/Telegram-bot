def зарегистрировать_чат(chat_id, название=None):
    """Запоминает группу где активен бот (фоновая запись — мгновенно)."""
    _записать(
        "INSERT INTO chats (chat_id, name) VALUES (%s, %s) ON CONFLICT (chat_id) DO NOTHING",
        (str(chat_id), название or str(chat_id))
    )

def разослать_всем(текст, исключить=None):
    """Отправляет сообщение во все зарегистрированные группы."""
    rows = _выполнить("SELECT chat_id FROM chats", fetch="all") or []
    успехи, ошибки = 0, 0
    for row in rows:
        cid = int(row["chat_id"])
        if исключить and cid == исключить:
            continue
        try:
            bot.send_message(cid, текст, parse_mode="Markdown")
            успехи += 1
        except Exception as e:
            log.warning(f"Рассылка в {cid} не удалась: {e}")
            ошибки += 1
    return успехи, ошибки

