        # Ожидание кода авторизации
        if ждёт_код.get(user_id):
            ввод_код = message.text.strip().lower()
            правильный_код = получить_рег_код().lower()
            if ввод_код == правильный_код:
                ждёт_код.pop(user_id, None)
                ждёт_имя[user_id] = True
