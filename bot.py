def дать_ачивку(chat_id, user_id, имя, ачивка_id):
    if ачивка_id not in ДОСТИЖЕНИЯ:
        return
    статы = загрузить_статы()
    игрок = получить_игрока(статы, user_id, имя)
    if ачивка_id not in игрок["ачивки"]:
        игрок["ачивки"].append(ачивка_id)
        сохранить_статы(статы)
        а = ДОСТИЖЕНИЯ[ачивка_id]
        try:
            bot.send_message(
                chat_id,
                f"🏅 *Новое достижение разблокировано!*\n\n"
                f"{а['emoji']} *{а['название']}*\n"
                f"_{а['описание']}_",
                parse_mode="Markdown"
            )
        except Exception as e:
            log.error(f"Ошибка отправки ачивки: {e}")
        # Проверяем: 10+ ачивок → выдаём титул «тру бигфут»
