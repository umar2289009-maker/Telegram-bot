        user_id = message.from_user.id
        chat_id = message.chat.id
        имя = получить_имя(user_id) or message.from_user.first_name or "незнакомец"

        части = message.text.strip().split(maxsplit=1)
        if len(части) < 2:
            bot.send_message(chat_id,
                "❌ Укажи кого грабить.\n\nПример: `/ограбить Ник`",
                parse_mode="Markdown")
            return

        цель_ник = части[1].strip().lstrip("@")

        статы = загрузить_статы()
        атак = получить_игрока(статы, user_id, имя)

        # Найти жертву
        цель_uid, цель_д, _ = найти_игрока_по_нику(цель_ник)
        if цель_uid is None:
            bot.send_message(chat_id, f"❌ Игрок *{цель_ник}* не найден.", parse_mode="Markdown")
            return

        if str(цель_uid) == str(user_id):
            bot.send_message(chat_id, "😐 Себя грабить? Серьёзно?")
            return

        цель_имя = цель_д.get("имя", цель_ник)
        цель_монет = цель_д.get("мэлкоины", 0)
        атак_монет = атак.get("мэлкоины", 0)

        if цель_монет < ОГРАБ_МИН_МОНЕТ:
            bot.send_message(chat_id,
                f"❌ У *{цель_имя}* слишком мало монет для ограбления (нужно ≥{ОГРАБ_МИН_МОНЕТ}🪙).",
                parse_mode="Markdown")
            return

        if атак_монет < ОГРАБ_МИН_АТАК:
            bot.send_message(chat_id,
                f"❌ Нужно хотя бы *{ОГРАБ_МИН_АТАК}*🪙 чтобы рисковать — у тебя только {атак_монет}🪙.",
                parse_mode="Markdown")
            return

        # Проверить кулдаун
        import time as _time
        сейчас = int(_time.time())
        ограбления = атак.setdefault("ограбления", {})
        последний = ограбления.get(str(цель_uid), 0)
        прошло = сейчас - последний
