def _подключиться():
    return psycopg2.connect(os.environ["DATABASE_URL"])

# ─── Основное соединение (только для чтения) ──────────────────────────────────
conn = _подключиться()
_conn_lock = threading.Lock()

def _выполнить(sql, params=(), fetch=None):
    """Синхронное выполнение — только для SELECT-запросов."""
    global conn
    with _conn_lock:
        for _ in range(2):
            try:
                cur = conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor)
                cur.execute(sql, params)
                conn.commit()
                if fetch == "one":
                    return cur.fetchone()
                if fetch == "all":
                    return cur.fetchall()
                return None
            except psycopg2.OperationalError:
                conn = _подключиться()
            except Exception as e:
                conn.rollback()
                log.error(f"DB ошибка: {e}")
                raise

# ─── Фоновая очередь записи (мгновенный возврат для обработчиков) ─────────────
_ОЧЕРЕДЬ_ЗАПИСИ: queue.Queue = queue.Queue()

def _записать(sql: str, params: tuple = ()):
    """Ставит запрос в очередь — возвращается МГНОВЕННО, не блокируя бота."""
    _ОЧЕРЕДЬ_ЗАПИСИ.put((sql, params))

def _фоновый_писатель():
    """Фоновый поток: читает из очереди и пишет в БД."""
    bg_conn = _подключиться()
    while True:
        try:
            item = _ОЧЕРЕДЬ_ЗАПИСИ.get(timeout=5)
        except queue.Empty:
            continue
        sql, params = item
        for _ in range(2):
            try:
                cur = bg_conn.cursor()
                cur.execute(sql, params)
                bg_conn.commit()
                break
            except psycopg2.OperationalError:
                try:
                    bg_conn = _подключиться()
                except Exception:
                    pass
            except Exception as e:
                try:
                    bg_conn.rollback()
                except Exception:
                    pass
                log.error(f"BG write error: {e}")
                break

_bg_writer_thread = threading.Thread(target=_фоновый_писатель, daemon=True, name="db-writer")
_bg_writer_thread.start()

