_РЕГ_КОД_КЭШ: Optional[str] = None
_рег_код_lock = threading.Lock()

def получить_рег_код() -> str:
    """Возвращает текущий код регистрации (из кэша или БД)."""
    global _РЕГ_КОД_КЭШ
    with _рег_код_lock:
        if _РЕГ_КОД_КЭШ is not None:
            return _РЕГ_КОД_КЭШ
        row = _выполнить("SELECT value FROM settings WHERE key = 'reg_code'", fetch="one")
        код = row["value"] if row else "мэлкоины"
        _РЕГ_КОД_КЭШ = код
        return код

def установить_рег_код(новый_код: str):
    """Сохраняет новый код регистрации в кэш и БД."""
    global _РЕГ_КОД_КЭШ
    with _рег_код_lock:
        _РЕГ_КОД_КЭШ = новый_код
    _записать(
        "INSERT INTO settings (key, value) VALUES ('reg_code', %s) "
        "ON CONFLICT (key) DO UPDATE SET value = EXCLUDED.value",
        (новый_код,)
    )

# Постоянный кэш игроков — загружается из БД один раз при старте
# Остаётся в памяти всё время работы бота → команды отвечают мгновенно
_КЭШСТАТОВ: dict = {}   # user_id -> data (живые данные)
_ДБ_СНИМОК: dict = {}   # user_id -> json-строка последнего сохранения в БД
_КЭШ_ЛОК = threading.Lock()  # защита от race condition при многопоточной записи

def _прогреть_кэш():
    """Загружает всех игроков из БД в память при старте — один раз."""
    rows = _выполнить("SELECT user_id, data FROM players", fetch="all") or []
    for row in rows:
        uid = row["user_id"]
        data = row["data"] if isinstance(row["data"], dict) else json.loads(row["data"])
        _КЭШСТАТОВ[uid] = data
        _ДБ_СНИМОК[uid] = json.dumps(data, ensure_ascii=False, sort_keys=True)
    log.info(f"Кэш игроков загружен: {len(_КЭШСТАТОВ)} игроков ✅")

