def _прогреть_кто_ты():
    rows = _выполнить("SELECT user_id, date, character FROM kto_ty", fetch="all") or []
    for row in rows:
        _КТО_ТЫ_КЭШ[row["user_id"]] = (row["date"], row["character"])

def _прогреть_лох_дня():
    rows = _выполнить("SELECT chat_id, date, name, user_id FROM loh_dnya", fetch="all") or []
    for row in rows:
        _ЛОХ_ДНЯ_КЭШ[row["chat_id"]] = (row["date"], row["name"], row["user_id"])

def получить_персонажа(user_id):
    сегодня = str(date.today())
    cached = _КТО_ТЫ_КЭШ.get(str(user_id))
    if cached and cached[0] == сегодня:
        return cached[1]
    персонаж = random.choice(list(КТО_ТЫ.keys()))
    _КТО_ТЫ_КЭШ[str(user_id)] = (сегодня, персонаж)
    _записать(
        "INSERT INTO kto_ty (user_id, date, character) VALUES (%s, %s, %s) "
        "ON CONFLICT (user_id) DO UPDATE SET date = EXCLUDED.date, character = EXCLUDED.character",
        (str(user_id), сегодня, персонаж)
    )
    return персонаж

