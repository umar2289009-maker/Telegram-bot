def обновить_стат(chat_id, user_id, имя, выжил, выстрелов):
    статы = загрузить_статы()
    игрок = получить_игрока(статы, user_id, имя)
    игрок["имя"] = имя
    игрок["игр"] += 1
    # Обновляем рекорд один раз здесь
    if выстрелов > игрок.get("рекорд", 0):
        игрок["рекорд"] = выстрелов
    if выжил:
        игрок["выжил"] += 1
        награда = 50 * выстрелов
        сохранить_статы(статы)
        начислить_мэлкоины(user_id, имя, награда, chat_id)
        try:
            bot.send_message(chat_id, f"💰 +{награда} мэлкоинов за победу в рулетке! Изи бриджи 😎")
        except Exception:
            pass
    else:
        сохранить_статы(статы)
    проверить_ачивки_рулетка(chat_id, user_id, имя, выжил, выстрелов)

